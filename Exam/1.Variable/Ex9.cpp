#include <iostream>
#include <math.h>
const float PI = 3.14 ;
using namespace std ;

int main ()
{
    float r ;
    cin >> r ;
    cout << " Chu vi hinh tron = " << 2 << " * " << r << " * " << PI << " = " << 2 * r * PI << endl;
    cout << " Dien tich hinh tron = " << r << " * " << r << " * " << PI << " = " << r * r * PI ;
    return 0 ;
}